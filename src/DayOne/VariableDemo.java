package DayOne;

public class VariableDemo {

	public static void main(String[] args) {


		int a = 2;
		int b = 3;
	
		a =  a+b;    // a = 5
		b =  a-b;    // b = 2
		a =  a-b;    // a = 3
		
		
		
		//swapping of two number 
		
		System.out.println(a); //3
		System.out.println(b); //2
		
		
		
	}

}
